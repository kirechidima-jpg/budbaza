open(my $in, '<:encoding(UTF-8)', 'js/data.js') or die $!;
my @lines = <$in>;
close $in;

my ($img_replaced, $img_added, $desc_ok) = (0, 0, 0);

for my $ln (keys %replace_img) {
  my $idx = $ln - 1;
  my $url = $replace_img{$ln};
  if ($lines[$idx] =~ s/img:"[^"]*"/img:"$url"/) { $img_replaced++; }
  else { print "WARN replace_img line $ln not matched\n"; }
}

for my $ln (keys %add_img) {
  my $idx = $ln - 1;
  my $url = $add_img{$ln};
  if ($lines[$idx] =~ s/,subcat:/,img:"$url",subcat:/) { $img_added++; }
  elsif ($lines[$idx] =~ /img:/) { print "SKIP line $ln (already has img)\n"; $img_added++; }
  else { print "WARN add_img line $ln not matched\n"; }
}

for my $ln (sort { $a <=> $b } keys %descs) {
  my $idx = $ln - 1;
  my $text = $descs{$ln};
  if ($lines[$idx] =~ s/desc:".*?",stock:/desc:"$text",stock:/) { $desc_ok++; }
  else { print "WARN desc line $ln not matched\n"; }
}

open(my $out, '>:encoding(UTF-8)', 'js/data.js') or die $!;
print $out @lines;
close $out;
print "done: $img_replaced img replaced, $img_added img added, $desc_ok descs written\n";
