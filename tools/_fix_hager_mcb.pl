my ($img_ok, $desc_ok) = (0, 0);

for my $ln (keys %update_img) {
  my $idx = $ln - 1;
  my $url = $update_img{$ln};
  if ($lines[$idx] =~ s/img:"[^"]*"/img:"$url"/) { $img_ok++; }
  elsif ($lines[$idx] =~ s/,subcat:/,img:"$url",subcat:/) { $img_ok++; }
  else { print "WARN img line $ln not matched\n"; }
}

for my $ln (keys %descs) {
  my $idx = $ln - 1;
  my $text = $descs{$ln};
  if ($lines[$idx] =~ s/desc:".*?",stock:/desc:"$text",stock:/) { $desc_ok++; }
  else { print "WARN desc line $ln not matched\n"; }
}

